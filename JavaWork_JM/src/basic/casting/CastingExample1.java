package basic.casting;

public class CastingExample1 {
	public static void main(String[] args) {
		
		byte b = 10;
		int i = b; // 작은 타입을 큰 타입에 대입할 때는 자동변환(promotion,UpCasting)
		System.out.println(i);
		
		char c = 'a';
		int j = c;
		System.out.println("A,의 문자 번호 "+ j);
		
		int k = 500;
		double d = k;
		System.out.println(d);
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
